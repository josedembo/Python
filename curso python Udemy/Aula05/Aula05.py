'''
                Operadores artiméticos
+ adição
- subtração
* multiplicação
/ divisão
// divisão inteira
** Exponenciação
% resto
() parentes:oredem de precedência
'''

print('Adição +', 10 + 10)  # adição
print('multiplicação *', 10 * 10)  # multiplicação
print('divisão /',10 / 3)  # divisão
print('subtração',10 - 10)  # subtração
print('divisão inteira //', 10 // 3)  # divisão inteira
print('expoente **', 10 ** 2)  # exponenciação
print('resto da divisão', 10 % 3)
print(5+2*10)
print((5+2)*10)

